SELECT 
    P.PRODUCT_ID,
    P.PRODUCT_NAME,
    O.TOTAL * P.PRICE AS TOTAL_SALES
FROM
    FOOD_PRODUCT P
JOIN
    (
    SELECT
        PRODUCT_ID,
        SUM(AMOUNT) AS TOTAL
    FROM
        FOOD_ORDER
    WHERE
        PRODUCE_DATE >= '2022-05-01' AND PRODUCE_DATE < '2022-06-01'
    GROUP BY
        PRODUCT_ID
    ) AS O ON P.PRODUCT_ID = O.PRODUCT_ID
ORDER BY 
    TOTAL_SALES DESC, P.PRODUCT_ID;