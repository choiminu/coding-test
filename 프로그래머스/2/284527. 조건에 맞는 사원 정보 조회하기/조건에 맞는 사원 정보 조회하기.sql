SELECT
    SCORE,
    E.EMP_NO,
    EMP_NAME,
    POSITION,
    EMAIL
FROM
    HR_EMPLOYEES E
JOIN (
    SELECT 
        EMP_NO,
        SUM(SCORE) AS SCORE
    FROM
        HR_GRADE
    WHERE
        YEAR = 2022
    GROUP BY
        EMP_NO
    ORDER BY
        SUM(SCORE) DESC
    LIMIT 1
) G ON G.EMP_NO = E.EMP_NO
